# Arithmetic operators supported in Python 3
# Addition
# Subtraction
# Multiplication
# Division         : 2 types - one which gives floating point result (operator = /), one which gives integer result (operator = //)
# Modulo
# Exponentiation

print('Addition:', 10+3)
print('Subtraction:', 10-3)
print('Multiplication:', 10*3)
print('Division Float:', 10/3)
print('Division Integer:', 10//3)
print('Modulo:', 10%3)
print('Exponentiation:', 10**3)

# Precedence of operators
# 1. Parenthesis
# 2. Exponentiation
# 3. Multiplication or Division
