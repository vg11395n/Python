# Built in functions supported in Python 3
# round()   : round up to higher end
# abs()     : absolute value
# Ceil()    : round up to higher end
# Floor()    : round down to higher end

import math
print('Round:', round(2.9))
print('Abs:', abs(-2.9))
print('Ceil:', math.ceil(2.9))
print('Floor:', math.floor(2.9))